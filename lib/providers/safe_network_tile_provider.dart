import 'dart:typed_data';
import 'package:flutter_map/flutter_map.dart';
import 'package:flutter/material.dart';

/// A tile provider that wraps network tile fetching and suppresses errors,
/// returning an empty image on failure to avoid exceptions.
class SafeNetworkTileProvider extends NetworkTileProvider {
  @override
  ImageProvider<Object> getImage(TileCoordinates coords, TileLayer options) {
    try {
      return super.getImage(coords, options);
    } catch (e) {
      // Return a 1x1 transparent image to suppress errors
      return MemoryImage(Uint8List.fromList(
          <int>[
            0x89,0x50,0x4E,0x47,0x0D,0x0A,0x1A,0x0A,0x00,0x00,0x00,0x0D,0x49,0x48,0x44,0x52,
            0x00,0x00,0x00,0x01,0x00,0x00,0x00,0x01,0x08,0x06,0x00,0x00,0x00,0x1F,0x15,0xC4,
            0x89,0x00,0x00,0x00,0x0A,0x49,0x44,0x41,0x54,0x78,0x9C,0x63,0x00,0x01,0x00,0x00,
            0x05,0x00,0x01,0x0D,0x0A,0x2D,0xB4,0x00,0x00,0x00,0x00,0x49,0x45,0x4E,0x44,0xAE,
            0x42,0x60,0x82
          ]
      ));
    }
  }
}
